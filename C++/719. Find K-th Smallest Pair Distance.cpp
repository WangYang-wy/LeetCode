#include "header.h"

class Solution {
private:
    int K;
    int count = 0;
    vector<int> heap;

    void init() {
        for (int i = 0; i < this->K; ++i) {
            this->heap.push_back(0);
        }
    }

    void push_back(int x) {
        if (this->count < this->K - 1) { // 加入后没满。
            this->heap[count] = x;
            count++;
        } else if (this->count == this->K - 1) { // 加入后满了。
            this->heap[count] = x;
            count++;
            this->head_sort();
        } else { // 堆排序。
            if (x < this->get_max()) { // 小于最小的 K 个数字。
                this->heap[0] = x;
                this->adjust(0);
            } else { // 更大，不需要。
                return;
            }
        }
    }

    void head_sort() {
        for (int i = this->K / 2 - 1; i >= 0; --i) {
            this->adjust(i);
        }
    }

    void adjust(int i) {
        while (i < this->K / 2) { // 是非叶子结点。
            int left = 2 * i + 1;
            int right = 2 * i + 2;
            int max_index = i;

            if (left < this->K && this->heap[left] > this->heap[i]) {
                max_index = left;
            }

            if (right < this->K && this->heap[right] > this->heap[i]) {
                max_index = right;
            }

            if (i != max_index) {
                int tmp = this->heap[i];
                this->heap[i] = this->heap[max_index];
                this->heap[max_index] = tmp;
                i = max_index;
            } else { // 如果自己已经是最大了，那么以后不需要调整了。
                break;
            }
        }
    }

    /**
    * 获取最大堆里最大的数字。
    */
    int get_max() {
        return this->heap[0];
    }

public:
    int smallestDistancePair(vector<int> &nums, int k) {
        int n = nums.size(), N = 1000000;
        vector<int> cnt(N, 0);
        for (int i = 0; i < n; ++i) {
            for (int j = i + 1; j < n; ++j) {
                ++cnt[abs(nums[i] - nums[j])];
            }
        }
        for (int i = 0; i < N; ++i) {
            if (cnt[i] >= k) {
                return i;
            }
            k -= cnt[i];
        }
        return -1;
    }

    int smallestDistancePair_heapSort_TLE(vector<int> &nums, int k) {
//        vector<int> ans;
        this->K = k;
        this->init();
        int n = int(nums.size());
        for (int i = 0; i < n; ++i) {
            for (int j = i + 1; j < n; ++j) {
                this->push_back(abs(nums[i] - nums[j]));
            }
        }

        return this->get_max();
    }

    int smallestDistancePair_TLE(vector<int> &nums, int k) {
        this->K = k;
        vector<int> ans;
        int n = int(nums.size());
        for (int i = 0; i < n; ++i) {
            for (int j = i + 1; j < n; ++j) {
                ans.push_back(abs(nums[i] - nums[j]));
            }
        }
        sort(ans.begin(), ans.end());
        return ans[k - 1];
    }
};

